class PartTimeJob {
  final String id;
  final String name;
  final String emoji;
  final int minAge;
  final double salary;
  final int hoursPerWeek;
  final int responsibilityRequired;
  final int experienceRequired;
  final int stressGain;
  final int experienceGain;
  final int customerSkillGain;

  const PartTimeJob({
    required this.id,
    required this.name,
    required this.emoji,
    required this.minAge,
    required this.salary,
    required this.hoursPerWeek,
    required this.responsibilityRequired,
    required this.experienceRequired,
    required this.stressGain,
    required this.experienceGain,
    required this.customerSkillGain,
  });
}
